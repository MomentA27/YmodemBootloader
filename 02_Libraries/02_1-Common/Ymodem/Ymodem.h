/** CRC校验 */
#define CRC_POLY 0x1021
#define CRC_INIT 0x0000

/** Ymodem协议常量定义 */
#define SOH     0x01    ///< 128字节数据包起始符
#define STX     0x02    ///< 1024字节数据包起始符
#define EOT     0x04    ///< 传输结束符
#define ACK     0x06    ///< 应答
#define NAK     0x15    ///< 重传请求
#define CAN     0x18    ///< 取消传输
#define CA      0x43    ///< 'C'字符，请求CRC模式

/** Ymodem协议参数 */
#define YMODEM_PACKET_SIZE_128    128    ///< SOH包数据长度
#define YMODEM_PACKET_SIZE_1024   1024   ///< STX包数据长度
#define YMODEM_MAX_RETRY          5      ///< 最大重试次数
#define YMODEM_RX_TIMEOUT_MS      1000   ///< 接收超时时间(毫秒)
#define YMODEM_FILE_NAME_MAX_LEN  64     ///< 文件名最大长度

/** Ymodem数据包最大长度 (1包头 + 1序号 + 1反序号 + 1024数据 + 2CRC) */
#define YMODEM_MAX_PACKET_SIZE    1029

#define YMODEM
