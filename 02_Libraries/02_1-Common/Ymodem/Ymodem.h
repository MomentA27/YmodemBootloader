/**
 * @brief Ymodem协议接收入口函数
 * @param base_addr Flash写入基地址 (需预先擦除)
 * @param max_size Flash最大可写入字节数
 * @return 接收到的文件大小(字节)，失败返回-1
 * @note 此函数会阻塞，直到Ymodem传输完成或超时中止
 * @note 零拷贝设计：数据直接从UART DMA缓冲区校验后写入Flash
 *  YModem 帧0（起始帧 / 块0）：
 *  ┌───────────────────────────────────────────────────────────────┐
 *  │ 字节  |  0   |   1   |   2   |    3~130     | 131~132 │
 *  │      | SOH  |  0x00 |  0xFF | 文件名+大小+填充|  CRC    │
 *  │      | 0x01 |  序号  |  补码 |   128字节      |  2字节   │
 *  └───────────────────────────────────────────────────────────────┘
 *  YModem STX 数据帧（1024字节模式）：
 *  ┌───────────────────────────────────────────────────────────────┐
 *  │ 字节  |  0   |   1    |    2   |      3~1026    | 1027~1028 │
 *  │      | STX  | 序列号  |  补码   |      数据       |   CRC     │
 *  │      | 0x02 | 0x01~FF| ~序列号 |    1024字节     |   2字节    │
 *  └───────────────────────────────────────────────────────────────┘
 *  YModem SOH 数据帧（128字节模式）：
 *  ┌───────────────────────────────────────────────────────────────┐
 *  │ 字节  |  0   |   1     |    2   |    3~130    | 131~132 │
 *  │      | SOH  | 序列号   |  补码   |     数据     |   CRC   │
 *  │      | 0x01 | 0x01~FF | ~序列号 |   128字节    |   2字节  │
 *  └───────────────────────────────────────────────────────────────┘
 *  YModem 结束帧（SOH空包）：
 *  ┌───────────────────────────────────────────────────────────────┐
 *  │ 字节  |  0   |   1    |    2     |    3~130    | 131~132     │
 *  │      | SOH  | 下一号  |  ~下一号   |  全0x00填充  |  CRC=0x0000 │
 *  │      | 0x01 |        |          |   128字节    |   2字节      │
 *  └───────────────────────────────────────────────────────────────┘
 */
int32_t Ymodem_Receive(uint32_t base_addr, uint32_t max_size);

/**
 * @brief 获取文件信息结构体指针
 * @return 文件信息结构体指针
 */
const ymodem_file_info_t* Ymodem_GetFileInfo(void);
